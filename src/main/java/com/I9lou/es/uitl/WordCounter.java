package com.I9lou.es.uitl;
//词语计数器
public class WordCounter {

    //词语个数
    private Integer count =0;

    //词语类型
    private int type= 0;

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
